
def area(a):
    """Returns an area of given square

    Args:
        a (int/float): side of the square

    Returns:
        int/float: area of the square
    """
    return a * a


def perimeter(a):
    """Returns a perimeter of given square

    Args:
        a (int/float): side of the square

    Returns:
        int/float: perimeter of the square
    """
    return 4 * a
